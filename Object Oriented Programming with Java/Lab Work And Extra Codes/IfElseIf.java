public class IfElseIf {
	public static void main(String[] args) {
		String lightcolor = "Green";
		
		if(lightcolor=="Green")
		{  System.out.println("Go");	}
		
		else if(lightcolor=="Yellow")
		{System.out.println("Slow down");}
		
		else if(lightcolor=="Red")	
		{System.out.println("Stop");}	
	}
}
